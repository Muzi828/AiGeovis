def _tier_label(tier: str, lang: Optional[str]) -> str:
    """返回层级（国家/城市/机构，含 affiliation_ 前缀）在当前语言下的标签。"""
    zh = {**_TIER_LABELS_ZH, **_AFFIL_LABELS_ZH}
    en = {**_TIER_LABELS_EN, **_AFFIL_LABELS_EN}
    return (en if _is_en(lang) else zh).get(tier, tier)


