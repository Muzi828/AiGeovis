def _is_en(lang: Optional[str]) -> bool:
    return str(lang or "zh").lower().startswith("en")


def _tr(lang: Optional[str], zh: str, en: str) -> str:
    """按语言返回对应文案。"""
    return en if _is_en(lang) else zh


_TIER_LABELS_ZH = {"country": "国家", "city": "城市", "org": "机构"}
